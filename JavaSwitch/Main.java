public class Main {
	public static void main(String[] args) {
		String season = "Autumn";
		switch (season) {

			case "Winter":
				System.out.println("it's 12, 01, 02");
				break;
			case "Spring":
				System.out.println("it's 03, 04, 05");
				break;
			case "Summer":
				System.out.println("it's 06,07,08");
				break;
			case "Autumn":
				System.out.println("it's 09, 10, 11");
				break;
		}
	}
}
